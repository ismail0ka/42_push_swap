#include "push_swap.h"

int	is_sorted(t_stack *s)
{
	int	i;

	i = 0;
	while (i < s->size - 1)
	{
		if (s->bp[i] < s->bp[i + 1])
			return (0);
		i++;
	}
	return (1);
}
